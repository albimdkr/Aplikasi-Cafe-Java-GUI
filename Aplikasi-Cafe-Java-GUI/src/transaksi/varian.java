package transaksi;

public class varian {
    int no, harga;
    String varian;

    public varian(int no, int harga, String varian) {
        this.no = no;
        this.harga = harga;
        this.varian = varian;
    }

    public int getNo() {
        return no;
    }

    public int getHarga() {
        return harga;
    }

    public String getVarian() {
        return varian;
    }
    
}
